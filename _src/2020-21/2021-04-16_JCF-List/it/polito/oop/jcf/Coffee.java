package it.polito.oop.jcf;

public class Coffee {
	private String type;
	private String brand;
	private double internalAttribue;
	
	public Coffee(String type, String brand) {
		this.type = type;
		this.brand = brand;
	}

	public Coffee(String type) {
		this.type = type;
	}

	public double getInternalValueUsedSometime() {
		return internalAttribue;
	}

	public void setInternalValueUsedSometime(double intensity) {
		this.internalAttribue = intensity;
	}

	public String getType() {
		return type;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	@Override
	public String toString() {
		return type + " [brand=" + brand + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((brand == null) ? 0 : brand.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!(obj instanceof Coffee))
			return false;
		Coffee other = (Coffee) obj;
		if (brand == null) {
			if (other.brand != null)
				return false;
		} else if (!brand.equals(other.brand))
			return false;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}

	
	
}
