package it.oop.polito.arrays;

public class DummyClass {
	private int foo;
	private int bar;
	
	public DummyClass(int foo, int bar) {
		this.foo = foo;
		this.bar = bar;
	}

	public int getFoo() {
		return foo;
	}

	public void setFoo(int foo) {
		this.foo = foo;
	}

	public int getBar() {
		return bar;
	}

	public void setBar(int bar) {
		this.bar = bar;
	};
	
	
}
